#include "libft.h"

char	*ft_strchr(char *s, int n)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == n)
			return (&s[i]);
		i++;
	}
	if (s[i] == 0)
	{
		return (&s[i]);
	}
	return (NULL);
}	
